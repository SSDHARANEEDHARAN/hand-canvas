const STORAGE_KEY = "gesture-theme-v1";

export function getThemePreset(themeId?: string | null): ThemePreset {
  return THEME_PRESETS.find((theme) => theme.id === themeId) ?? THEME_PRESETS[0];
}

export function loadThemeId(): string {
  if (typeof window === "undefined") return THEME_PRESETS[0].id;
  return window.localStorage.getItem(STORAGE_KEY) ?? THEME_PRESETS[0].id;
}

export function saveThemeId(themeId: string) {
  if (typeof window === "undefined") return;
  window.localStorage.setItem(STORAGE_KEY, themeId);
}

export function applyTheme(theme: ThemePreset) {
  if (typeof document === "undefined") return;
  const root = document.documentElement;
  Object.entries(theme.vars).forEach(([name, value]) => {
    root.style.setProperty(`--${name}`, value);
  });
}
